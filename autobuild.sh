#!/bin/bash
# =============================================================================
# autobuild.sh — Скрипт автоматической пересборки сайта
# Хостинг: Beget / Путь: /home/s/shvetsx9/ratingtops.casino/
#
# Назначение:
#   Ставится в планировщик задач (Cron) на хостинге Beget.
#   При каждом запуске: устанавливает зависимости и пересобирает статику.
#   Результат складывается в public_html/ (см. astro.config.mjs → outDir).
#
# Рекомендуемое расписание Cron: 1 раз в месяц (1-го числа в 3:00)
#   0 3 1 * * /home/s/shvetsx9/ratingtops.casino/autobuild.sh >> /home/s/shvetsx9/ratingtops.casino/autobuild.log 2>&1
# =============================================================================

set -e  # Прерывать выполнение при любой ошибке

# ── Инициализация NVM (Node Version Manager) ─────────────────────────────────
# Cron запускается без пользовательского окружения, поэтому PATH не содержит
# node и npm. Эти две строки загружают NVM вручную, делая node/npm доступными.
export NVM_DIR="$HOME/.nvm"
[ -s "$NVM_DIR/nvm.sh" ] && \. "$NVM_DIR/nvm.sh"

# ── Директория проекта ────────────────────────────────────────────────────────
PROJECT_DIR="/home/s/shvetsx9/ratingtops.casino"

echo "========================================"
echo "[autobuild] Запуск: $(date '+%Y-%m-%d %H:%M:%S')"
echo "========================================"

# ── 1. Переход в рабочую директорию ──────────────────────────────────────────
cd "$PROJECT_DIR" || {
  echo "[ERROR] Не удалось перейти в $PROJECT_DIR"
  exit 1
}
echo "[OK] Рабочая директория: $(pwd)"

# ── 2. Установка/обновление зависимостей ─────────────────────────────────────
echo "[...] npm install"
npm install --prefer-offline --no-audit --no-fund
echo "[OK] Зависимости установлены"

# ── 3. Сборка статики → public_html/ ─────────────────────────────────────────
echo "[...] npm run build"
npm run build
echo "[OK] Сборка завершена → public_html/"

echo "========================================"
echo "[autobuild] Готово: $(date '+%Y-%m-%d %H:%M:%S')"
echo "========================================"
